<img class="fr" alt="wolf" src="https://upload.wikimedia.org/wikipedia/commons/5/53/Scandinavian_grey_wolf_Canis_lupus.jpg" width="30%" height="30%">
<p>In approximately 12000 BCE, the grey wolf was domesticated into the "Canis familiaris" (latin term for domesticated dog) by nomadic people who lived a hunter-gatherer lifestyle.</p>

<p>This domestication happened before the domestication of sheep, cows, goats, and many other farm animals.</p>

<p>Domestication is when one species has large influence over another in ways such as controlling reproduction and care.
	There are many theories on why this initially happened, but one is that wolves ate people's food scraps, and gave indirect protection by killing
	nearby predators. Then they were more likely to live if they were tamer to people, leading to their initial domestication.</p>

<h3>Why So many Breeds?</h3>

<p>After this initial domestication of the Grey Wolf, they were split into many different breeds. In fact, there are more dog breeds than breeds of
	any other animal. But why? This split happened because they dogs were wanted/needed for many different reasons.</p>
<ul>
	<li>Some domesticated into hunting dogs, which are used to hunt or help hunters, such as terriers and retrievers.</li>
	<li>Some domesticated into toy dogs, which are a type of dog bred down in size to be a pet.</li>
	<li>Some domesticated into Pastoral Dogs which is a type of dog to help with livestock and herding.</li>
	<li>Some domesticated into Guard Dogs, which is a type of dog for protecting people and property.</li>
	<li>Some domesticated into Sled Dogs, which have the job of pulling sleds.</li>
</ul>

<h3>Differences from a Wolf</h3>

<p>A dog is different from a wolf in many ways. One is that it is domesticated to be
	friendlier to people, less aggressive, and to be more playful.
	Also, docile dogs have less stiff ears, and dogs have smaller craniums than a wolf of the same weight.
	</p>
<p>Domesticated dogs have a changed diet, and are able to eat starch and other human-foods because of a genetic change
	(in <!--<a href="https://en.wikipedia.org/wiki/AMY2B">-->Alpha-Amylase 2B<!--</a>-->) which allows them to digest it. This is speculated to have happened because early dogs would have been
	able to exploit eating crops of early human agriculture.</p>
