<p>The Spaniel is a type of dog called a "gun dog". Gun dogs are used for helping hunting,
	commonly by finding prey or scaring prey into the hunter.</p>

<p>In the late 1700s, they were divided into water and land breeds. Land breeds helped hunting on land,
	while water breeds helped hunt in water, and also frequently fetched shot fowl from the water.</p>

