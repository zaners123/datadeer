<h1>Submitting a MadLibs&#8482;</h1>
<h2>
If you want to donate a MadLibs&#8482;, follow this syntax then email it (as text or an attachment) to <a href="mailto:admin@datadeer.net">admin@datadeer.net</a>.
If you submit a good one, I'll add you to the <a href="/other/about.php">Thanks Page</a>!

<br><br>

To submit your own Mad Libs&#8482;, first open a text editor (Word&#8482;, Notepad&#8482;, Notepad++&#8482;, GEdit&#8482;, etc).

<br><br>

Then type in a story, but put replaced words in dollar signs. For example replace "Joe" with "$name$" (without the quotes).

<br><br>

If you want multiple occurrences of one word with one question (<a href="index.php">see the example MadLibs&#8482;</a>) then put the same number at the end for each one. If it doesn't repeat you don't need a number.

<br><br>

This is hard to explain, so <a href="index.php">Go to the Example</a> to see how it works. The next paragraph is what I used to make the example.

<br><br>

I went to the $adjective$ $food$ factory in $month$ with my friend $name$. We saw a $food1$ and ate it. The $food1$ tasted good.</h2>

