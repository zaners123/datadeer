<h1>Making a Website</h1>
<h2>If you want to know how to make a simple website (PHP,HTML,MYSQL,etc.) it can be easy.</h2>
<br>
First, learn how to program. It doesn't matter what language you choose (scratch, python, Java, Javscript, batch, etc).<br>
Make your first "Hello World", which is where you program something simply to say "Hello World".<br>
Learn more complex skills like arrays, sorting algorithms, and binary.<br>
Program your first simple game (Tetris, Tic-Tac-Toe, Chess). It gan be a text-based terminal game or a visual game.<br>
<br>
If you want to really go for a deep dive, <a href="linux.php">Get Linux</a>. This is the operating system of programmers.
<br><br>
Learn more programming languages so you understand more about what computers are capable of doing and the advantages of different languages.
<br><br>
At this point, you could start trying to make a server. Install a server OS like Ubuntu Server 18 or windows server 2016 on a PC you have available. Having a small HTTP server requires very little hardware (<40GB disk, <1GB ram, no graphics card). On this computer, set up a cool looking "Hello World" and ask some people to view it.
<br><br>
Once you have a website you plan on keeping, you could (But don't have to!) buy a domain name. This is much easier to remember than an IP address and something to advertise. They cost about a dollar a month so nothing too expensive.