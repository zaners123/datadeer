<h1>Information Technology Certifications</h1>
<div class="textful">
Why should you get a tech certification?<br>
&emsp;They are an easy way to show you know what you're doing and are very important to employers.<br>
What certifications should I get?<br>
&emsp;It really depends on what you plan on doing with your life, but for anything IT,
I would recommend starting with a <a href="https://certification.comptia.org/landing/aplus/">CompTia A+ Computer Repair Certification</a>.
This is because you learn a wide scope of important concepts, like:
<ul>
	<li>Hardware (CPU, memory, HDD, PCI, PSU)</li>
	<li><a href="buildYourPC.php">Building a PC</a></li>
	<li>Procedures</li>
	<li>Troubleshooting (software, hardware, network)</li>
</ul>
Having this certification would show you are good with the basics of technology.
From this, you would want to branch out to more complex and specific certifications.
You can get like 4 of these things in a year if you do it right. Here's a list I'm following:
<ol>
	<li><a href="https://certification.comptia.org/landing/aplus/">CompTIA A+</a></li>
	<li><a href="https://certification.comptia.org/certifications/network">CompTIA Network+</a></li>
	<li><a href="https://certification.comptia.org/certifications/linux">CompTIA Linux+</a></li>
	<li><a href="https://certification.comptia.org/certifications/security">CompTIA Security+</a></li>
	<li><a href="https://certification.comptia.org/certifications/server">CompTIA Server+</a></li>
	<li><a href="https://certification.comptia.org/certifications/cloud">CompTIA Cloud+</a></li>
</ol>
If you get these, it will be much easier to get an IT job.
