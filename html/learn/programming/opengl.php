<h1>OpenGL Programming on Linux</h1>
The hardest part is getting all the dependencies. After that, it's smooth sailing.

To get the dependencies, run:
<pre>
	apt-get install libgl1-mesa-dev libglew-dev libglm-dev freeglut3-dev libglu1-mesa-dev libgle3 libglfw3-dev libglew-dev libglm-dev libdevil-dev
</pre>

Then, you're good; that's mostly it

